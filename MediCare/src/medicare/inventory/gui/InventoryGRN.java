    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

//        String grn_id = jTextField1.getText();
        String Supplier = String.valueOf(jComboBox1.getSelectedItem());
//        String employer =  "gihanjithk@gmail.com";
        String drugId = String.valueOf(jComboBox2.getSelectedItem());
        double qty = Double.parseDouble(jFormattedTextField4.getText());
        double SellingPrice = Double.parseDouble(jFormattedTextField2.getText());
        double BuyingPrice = Double.parseDouble(jFormattedTextField3.getText());
        Date mfd = jDateChooser1.getDate();
        Date exp = jDateChooser2.getDate();
        String brand = jTextField5.getText();
        String drugName = jTextField8.getText();

        if (Supplier.equals("SELECT")) {
            JOptionPane.showMessageDialog(this, "PLease Select Supplier", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (drugId.equals("SELECT")) {
            JOptionPane.showMessageDialog(this, "PLease Select Drug Id", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (String.valueOf(qty) == null) {
            JOptionPane.showMessageDialog(this, "PLease Enter Quantity", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (String.valueOf(SellingPrice) == null) {
            JOptionPane.showMessageDialog(this, "PLease Enter Selling price", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (String.valueOf(BuyingPrice) == null) {
            JOptionPane.showMessageDialog(this, "PLease Enter Buying price", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (mfd == null) {
            JOptionPane.showMessageDialog(this, "PLease Select MFD", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (exp == null) {
            JOptionPane.showMessageDialog(this, "PLease Select EXP", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            GRNItems gRNItems = new GRNItems();

            gRNItems.setBrand(brand);
            gRNItems.setBuying(BuyingPrice);
            gRNItems.setSelling(SellingPrice);
            gRNItems.setDrugId(drugId);
            gRNItems.setExp(exp);
            gRNItems.setDrugName(drugName);
            gRNItems.setMfd(mfd);
            gRNItems.setQty(qty);

            if (grnItemMap.get(jComboBox2.getSelectedItem()) == null) {
                grnItemMap.put(String.valueOf(jComboBox2.getSelectedItem()), gRNItems);
                loadGRNItems();
            } else {

                GRNItems found = grnItemMap.get(jComboBox2.getSelectedItem());

                if (found.getExp().compareTo(exp) == 0
                        && found.getMfd().compareTo(mfd) == 0
                        && found.getBuying() == BuyingPrice
                        && found.getSelling() == SellingPrice) {

                    found.setQty(found.getQty() + qty);
                    loadGRNItems();
                    reset();
                } else {

                    JOptionPane.showMessageDialog(this, "GRN item already exists with diffrent dates and prices", "Error", JOptionPane.ERROR_MESSAGE);

                }
            }
        }


    }//GEN-LAST:event_jButton5ActionPerformed

