        String company_hotline = jFormattedTextField2.getText();
        String company_name = jTextField4.getText();

        if (company_hotline.isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLease Enter Company Hotline", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!company_hotline.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Mobile Number", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (company_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLease Enter Company Name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {

                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `company` WHERE `name` = '" + company_name + "' OR `hotline` = '" + company_hotline + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Already added Company", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL.executeIUD("INSERT INTO `company`(`name`,`hotline`) VALUES('" + company_name + "','" + company_hotline + "')");
                    JOptionPane.showMessageDialog(this, "Added Successfull", "Succes", JOptionPane.INFORMATION_MESSAGE);
                    reset();

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

