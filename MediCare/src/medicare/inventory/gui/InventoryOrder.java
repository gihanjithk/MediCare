    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String Supplier = String.valueOf(jComboBox1.getSelectedItem());
        String drugName = jTextField3.getText();
        String brand = String.valueOf(jComboBox2.getSelectedItem());
        double qty = Double.parseDouble(jFormattedTextField1.getText());
        String employee = jTextField5.getText();

        if (Supplier.equals("Select")) {
            JOptionPane.showMessageDialog(this, "PLease Select Supplier", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (drugName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "PLease Enter Drug name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "PLease Select Brand", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (String.valueOf(qty) == null) {
            JOptionPane.showMessageDialog(this, "PLease Enter Quantity", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            ORDERItems oRDERItems = new ORDERItems();

            oRDERItems.setDrugid(drugName);
            oRDERItems.setBrand(brand);
            oRDERItems.setQty(qty);
            oRDERItems.setEmployee(employee);

            if (orderItemsmap.get(jComboBox2.getSelectedItem()) == null) {
                orderItemsmap.put(String.valueOf(jComboBox2.getSelectedItem()), oRDERItems);
                loadOrdertems();

            } else {
                ORDERItems found = orderItemsmap.get(jComboBox2.getSelectedItem());

                found.setQty(found.getQty() + qty);
                loadOrdertems();
                reset();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

