    private void loadOrdertems() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        for (ORDERItems oRDERItems : orderItemsmap.values()) {
            Vector<String> vector = new Vector<>();
            vector.add(oRDERItems.getDrugid());
            vector.add(oRDERItems.getBrand());
            vector.add(String.valueOf(oRDERItems.getQty()));

            model.addRow(vector);
        }

    }

