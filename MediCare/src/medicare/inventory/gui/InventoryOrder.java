    private void loadBrand() {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `brand`");
            Vector<String> vector = new Vector<>();

            vector.add("Select");

            while (resultSet.next()) {

                vector.add(resultSet.getString("brand"));

            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loaddrug(String drugName, String brand) {

        try {

            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `drug` INNER JOIN `brand` ON "
                    + "`drug`.`brand_id` = `brand`.`id` WHERE `drug_name` LIKE '" + drugName + "%' AND `brand`.`brand` LIKE '" + brand + "%' ;");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("drug_name"));
                vector.add(resultSet.getString("brand.brand"));
                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

